							<li><p>10/2018 au 10/2019 Ingénieur développement logiciel <a  class="liensection" href="https://www.labsticc.fr/en/news/953-nooman-notifying-memories-for-dynamic-data-flow-application.htm" target="_blank" title=" Projet NOOMAN ">Projet NOOMAN </a> . Prise en main du simulateur de réseau sur puce architecture Von_Neumann <a  class="liensection" href="https://snipersim.org/w/The_Sniper_Multi-Core_Simulator" target="_blank" title=" SniperSim "> SniperSim </a>, pour le portage de codes  générés de type dataflow statique et dynamique sur des SOC/NOC à jeux d’instruction CISC intel X86_64.	                                     L’objectif était d’étudier des modèles ‘’éxotiques’’ d’architectures Von-Neumann simulés  pour  intégrer un nouveau composant et diminuer les érreurs d’accès caches mobilisant ~60% des ressources énergétiques journalière pour le rapatriement des données entre la RAM jusqu’aux cache de niveau 1 dans les différents niveaux de cache, le but d’intégrer un nouveau composant sur les cartes mères permettant de diminuer la charge de travail processeur du à ces requètes permanentes, successive et inutile, en définissant la mémoire vivre maitre sur le SOC et non le processeur, libérant ainsi la charge de travail de ce dernier.
J’ai porté la compilation GNU de ce simulateur sur Ubuntu 18,04, ais participé à l’encapsulation dans un docker avec un accès ssh et ai déverminé certaines étapes de la gestion des affinités du SMT Posix dans les générateurs de codes dataflow ( ORCC, Preesm ) . 
De mème, j’ai remonté des erreurs d’interprétation de micro-instructions assembleur (TZCNT – extension BMI1) dans le simulateur sniper-sim lors de la génération de fichiers de traces durant l’exécution de codes générés. </p></li>
							
