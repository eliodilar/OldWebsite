							Pour l’IFSTTAR l’ai travaillé sur l’intégration et le développement d’une couche de communication peer to peer wifi temps réel pour la simulation de communications entre véhicules autonomes dans le logiciel ADAS nommé <a  class="liensection" href="https://www.esi-group.com/fr/solutions-logicielles/environnement-virtuel/systemes-controles-virtuels/pro-sivictm" target="_blank" title=" ProSivic "> ProSivic </a> tournant simultanément sous OS Unix-like et Windows, intégré sur un <a class="liensection" href="images/bdijpg.jpg" target="_blank" title="Visuel simulateur"> simulateur</a> de pilotage develter dynamique et mécanique munit d’un casque VR SDK2 Oculus. 		
J’ai développé et intégré un SMT temps réel gérant l’acquisition physique de données et de capteurs, dans une mémoire partagé type DDS, par l’intermédiaire d’une .DLL, j’ai ensuite couplé ces données au moteur graphique MG-Engine du logiciel Pro-sivic par l’intermédiaire de canaux de propagations créés par le simulateur wifi temps réel NS3, pour permettre la récupération de données de vitesses, positions… entre les objets véhicules instanciés.								     
J’ai ensuite utilisé différents patterns comme le singleton pour créer un routage dynamique des données en cas de perte d’émétteurs.
